package com.loiane.curso.java.aula11;

public class VariaveisInteirasFlutuantes {

	public static void main(String[] args) {
		
		// muito utilizado na linguagem java
		double valorDaPassagemSG = 3.95;
		
		// não tão utilizado pois precisa por "f" no fim, um literal
		
		float valorDaPassagemNiteroi = 5.60f;
		
		System.out.println("Valor da passagem de São Gonçalo é: " + valorDaPassagemSG);
		
		System.out.println("Valor da passagem de Niterói é: " + valorDaPassagemNiteroi);
		
	}

}
